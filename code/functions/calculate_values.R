calculate_values <- function(max_count) {
  
#  cat("\n*****", max_count,"*****")
  
  thresholds <- c(     1,
                       5,
                       10,
                       25,
                       50, 
                       75, 
                       250, 
                       1000,
                       2500,
                       5000, 
                       7500, 
                       15000, 
                       20000, 
                       50000, 
                       60000, 
                       75000, 
                       200000, 
                       300000, 
                       750000, 
                       1000000, 
                       1500000, 
                       2000000, 
                       5000000, 
                       10000000 )
  
  values <- matrix(c( 1,1,1,
                      1,1,1,
                      2,2,1,
                      5,5,1,
                      10,10,1,
                      25,25,1,
                      50,50,1,
                      250,250,1,
                      500,500,1,
                      1000,1000,1,
                      2000,2000,1,
                      2500,2500,1,
                      5000,5000,1,
                      10000,10000,1,
                      15000,15000,1,
                      25000,25000,1,
                      50000,50000,1000,
                      75000,75000,1000,
                      150000,150000,1000,
                      250000,250000,1000,
                      300000,300000,1000,
                      500000,500000,1000,
                      1000000,1000000,1000,
                      1000000,1000000,1000 ), 
                   ncol = 3, byrow = TRUE)
  
  colnames(values) <- c("starting_value", "increment", "scaling_factor")
  
  index <- findInterval(max_count, thresholds)
  
 # Return the calculated values as a named list
  result <- as.list(values[index, ])
  return(result)
}
